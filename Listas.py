# Creamos Una lista de estudiantes
estudiantes = ['Jaimito', 'Juan', 'Carla', 'Valeria', 'Irene']
# Se imprime la lista
print(estudiantes)
# Se encuentra un estudiante por su posicion (buscar)
print(estudiantes[1])
# Se cambia el valor de un estudiante (editar)
estudiantes[2] = 'Patricia'
# Se imprime la lista
print(estudiantes)
# Se añade un nuevo estudiante (agregar)
estudiantes.append('Carlos')
# Se imprime la lista
print(estudiantes)
# Se eliminar un estudiante de la lista (borrar)
estudiantes.remove("Juan")
# Se imprime la lista
print(estudiantes)
# Se obtiene la longitud de la lista
print(len(estudiantes))
# Una lista puede tener varios tipos de datos
cosas = ['Tomates', 2, True, 'Plátanos', 3.14]
# Se imprime la lista
print(cosas)