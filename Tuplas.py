#Las tuplas son otro tipo de estructura de datos. Estas son ordenadas y no modificables.
#Cuando decimos que son ordenadas, es que al igual que con las listas podemos acceder a ellas mediante su posición.
#Las tuplas se inicializan usando paréntesis.

# Se crea la tupla
mis_estudiantes = ('Jaimito', 'Juan', 'Carla', 'Valeria', 'Irene', 'Juan')
# Se imprime la tupla
print(mis_estudiantes)
jaimito, juan, carla = ('Jaimito', 'Juan', 'Carla')
# Se imprime la variable "busqueda"
print(jaimito)
